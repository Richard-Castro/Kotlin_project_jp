fun main() {
    val myText = "Java" // Define uma variável chamada myText e atribui o valor "Java" a ela
    println(myText) // Imprime o conteúdo da variável myText = "Java"
    val newText = myText.replace("Ja", "Je") // Cria uma nova variável chamada newText, que contém a string myText com todas as ocorrências de "Ja" substituídas por "Je"
    println(newText) // Imprime o conteúdo da variável newText, que será "Jeva" após a substituição
}
